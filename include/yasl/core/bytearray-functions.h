QByteArray ba_toLower(const QByteArray & self)
{
  return self.toLower();
}

QByteArray ba_toUpper(const QByteArray & self)
{
  return self.toUpper();
}

QByteArray ba_trimmed(const QByteArray & self)
{
  return self.trimmed();
}

QByteArray ba_simplified(const QByteArray & self)
{
  return self.simplified();
}